package outils;

/**
 * Classe outils pair, permet de creer une pair de int.
 * @author benlacheheb
 *
 */
public class Pair {
	private int l;
	private int r;
	
	public Pair(int l,int r) {
		this.l = l;
		this.r = r;
	}

	public int getL() {
		return l;
	}

	public void setL(int l) {
		this.l = l;
	}

	public int getR() {
		return r;
	}

	public void setR(int r) {
		this.r = r;
	}
	
}
